export default class Company  {

  id: number;
  name:string;
  industry:string;
  location:string;
  size:string;
  userID:number;

  constructor(
    id: number,
    name:string,
    industry:string,
    location:string,
    size:string,
    userID:number
  ){
    this.id = id;
    this.name = name;
    this.industry = industry;
    this.location = location;
    this.size = size;
    this.userID = userID;
  }

};

